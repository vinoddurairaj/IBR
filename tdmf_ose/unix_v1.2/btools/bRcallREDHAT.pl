#!/usr/bin/perl  -Ipms
###############################################
#############################################
#
#  perl -w ./bRcallREDHAT.pl
#
#	call ssh scripts
#
###########################################
#############################################

#############################################
###########################################
#  vars  /  pms  /  use
###########################################
#############################################

eval 'require bRglobalvars';

eval $COMver;
eval $Bbnum;

#############################################
###########################################
#   STDOUT / STDERR
###########################################
#############################################

open (STDOUT, ">logs/callREDHAT.txt");
open (STDERR, ">&STDOUT");

#############################################
###########################################
#  exec statements 
###########################################
#############################################

print "\n\nSTART bRcallREDHAT.pl\n\n"; 

#############################################
###########################################
#  log values 
###########################################
#############################################

print "\n\nCOMver  =  $COMver\n\n";
print "\nBbnum     =  $Bbnum\n\n";

chdir "unix/redhat";
$PWD=qx|pwd|;
print "$PWD";

#############################################
###########################################
#  ssh scripts
###########################################
#############################################

system qq(perl bRsshRH4x32.pl);
system qq(perl bRsshRH4x64.pl);
system qq(perl bRsshRH4xia64.pl);
system qq(perl bRsshRH5xx64.pl);
system qq(perl bRsshRH5xx86.pl);
system qq(perl bRsshRH5xia64.pl);

###########################################
###########################################

print "\n\nEND bRcallREDHAT.pl\n\n"; 

###########################################
###########################################

close (STDERR);
close (STDOUT);
