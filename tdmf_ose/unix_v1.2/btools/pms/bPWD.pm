sub establishPWD {

   $uname=`uname`;

   if ($uname =~ m/^Linux|AIX|SunOS|HP/ ) {
      $CWD=`pwd`;
   }
   elsif ( $uname =~ m/CYGWIN/ ) {
      $CWD=`cd`;
      $CWD =~ s/^[A-Z,a-z]\://;
      $CWD =~ s/\\/\//g;
   }
   chomp ($CWD);
   return ($CWD);

} 

